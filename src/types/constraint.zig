/// Type inhabitation data for progressive typed generation.
/// This allows constrained generation to ensure expressions match expected types.
pub const TypeInhabitationData = struct {
    /// The goal type that generated code must satisfy
    goal_type: []const u8,

    /// Current partial type (updated during generation)
    current_type: ?[]const u8 = null,

    /// Available bindings in scope
    bindings: []const TypeBinding = &.{},

    /// Pre-computed token mask for current state
    token_mask: ?TokenMaskRules = null,

    /// Language for type syntax interpretation
    language: TypeLanguage = .typescript,

    /// Serialize to JSON for FFI transmission
    pub fn toJson(self: *const TypeInhabitationData, allocator: std.mem.Allocator) ![]u8 {
        var json = std.ArrayList(u8).init(allocator);
        const writer = json.writer();

        try writer.writeAll("{");
        try writer.print("\"goal_type\":\"{s}\"", .{self.goal_type});

        if (self.current_type) |ct| {
            try writer.print(",\"current_type\":\"{s}\"", .{ct});
        }

        try writer.writeAll(",\"bindings\":[");
        for (self.bindings, 0..) |binding, i| {
            if (i > 0) try writer.writeAll(",");
            try writer.print("{{\"name\":\"{s}\",\"type\":\"{s}\"}}", .{ binding.name, binding.type_sig });
        }
        try writer.writeAll("]");

        try writer.print(",\"language\":\"{s}\"", .{@tagName(self.language)});
        try writer.writeAll("}");

        return json.toOwnedSlice();
    }
};

/// Fingerprint for tracking constraint changes in incremental compilation
pub const ConstraintFingerprint = struct {
    /// Content hash of the constraint
    hash: u64,
    /// When the constraint was first created
    created_at: i64,
    /// When the constraint was last modified
    modified_at: i64,

    /// Compute a fingerprint from a constraint's content
    pub fn compute(constraint: *const Constraint) ConstraintFingerprint {
        var hasher = std.hash.Wyhash.init(0);

        // Hash all relevant fields that affect compilation
        hasher.update(constraint.name);
        hasher.update(constraint.description);

        const kind_bytes = std.mem.asBytes(&@intFromEnum(constraint.kind));
        hasher.update(kind_bytes);

        const source_bytes = std.mem.asBytes(&@intFromEnum(constraint.source));
        hasher.update(source_bytes);

        const enforcement_bytes = std.mem.asBytes(&@intFromEnum(constraint.enforcement));
        hasher.update(enforcement_bytes);

        const priority_bytes = std.mem.asBytes(&constraint.priority.toNumeric());
        hasher.update(priority_bytes);

        const severity_bytes = std.mem.asBytes(&@intFromEnum(constraint.severity));
        hasher.update(severity_bytes);

        return .{
            .hash = hasher.final(),
            .created_at = constraint.created_at,
            .modified_at = std.time.timestamp(),
        };
    }

    /// Check if this fingerprint differs from another
    pub fn hasChanged(self: ConstraintFingerprint, other: ConstraintFingerprint) bool {
        return self.hash != other.hash;
    }
};

/// A collection of constraints
pub const ConstraintSet = struct {
    constraints: std.ArrayList(Constraint),
    name: []const u8,
    allocator: std.mem.Allocator,

    pub fn init(allocator: std.mem.Allocator, name: []const u8) ConstraintSet {
        return .{
            .constraints = std.ArrayList(Constraint){},
            .name = name,
            .allocator = allocator,
        };
    }

    pub fn deinit(self: *ConstraintSet) void {
        // Note: Currently does not free individual constraint string fields
        // This is intentional - constraints may use static string literals (from Clew)
        // or allocated strings (from JSON parsing). For JSON-parsed constraints,
        // use an arena allocator that gets freed separately.
        self.constraints.deinit(self.allocator);
    }

    pub fn add(self: *ConstraintSet, c: Constraint) !void {
        var constraint = c;
        // Auto-assign a content-based ID if none was explicitly set
        if (constraint.id == 0) {
            constraint.id = constraint.computeId();
        }
        try self.constraints.append(self.allocator, constraint);
    }

    /// Clone this ConstraintSet, creating a deep copy with independent ownership.
    /// All string fields (name, description, origin_file) are duplicated so the
    /// clone is fully independent of the original's allocator.
    pub fn clone(self: *const ConstraintSet, allocator: std.mem.Allocator) !ConstraintSet {
        // Deep copy the set name
        const owned_name = try allocator.dupe(u8, self.name);
        errdefer allocator.free(owned_name);

        var cloned = ConstraintSet.init(allocator, owned_name);

        // Deep copy all constraints including their string fields
        for (self.constraints.items) |constraint| {
            var c = constraint;
            c.name = try allocator.dupe(u8, constraint.name);
            c.description = try allocator.dupe(u8, constraint.description);
            if (constraint.origin_file) |file| {
                c.origin_file = try allocator.dupe(u8, file);
            }
            try cloned.constraints.append(allocator, c);
        }

        return cloned;
    }
};
